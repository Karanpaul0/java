


public class Orderofoperations {

    public static void main(String[] args) {
        // ()  D  M  A  S
        // ()  division -> multiplication -> addition -> subtraction
        
        System.out.println(1 + 2 / 3 * 4 - 6);


        // () -> -> -> ->
        
        System.out.print(2/3/4/5/6/3/4);
    }
}
