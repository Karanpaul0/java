public class unary{
    public static void main(String[] args) {
        int x=4;
        int y=-x;
        System.out.println(y);

// post increment
        int a=2;
        // a=a+1; -> a++;
        a++;
        System.out.println(a);

        // pre increment
        int b=2;
        ++b;
        System.out.print(b);
    }
}