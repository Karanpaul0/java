
public class Arithmatic {

    public static void main(String[] args) {
        int a = 2;
        int b = 3;
        System.out.println("use of arithmatic operators");
        System.out.println("the value of a+b is :- " + (a + b));
        System.out.println("the value of a-b is :- " + (a - b));
        System.out.println("the value of axb is :- " + (a * b));
        System.out.println("the value of a/b is :- " + (a / b));


        double x=5;
        double y=3;
        System.out.println("use of arithmatic operators");
        System.out.println("the value of a+b is :- " + (x + y));
        System.out.println("the value of a-b is :- " + (x - y));
        System.out.println("the value of axb is :- " + (x * y));
        System.out.println("the value of a/b is :- " + (x / y));
    }
}
